package com.hwy.outPut;

public class ClueOutPutOperation {

    String normFlg;
    String serviceCodeType;
    String serviceCodeNormFlg;
    String marketingTitleName;
    String fileName;

    public String getNormFlg() {
        return normFlg;
    }

    public String getServiceCodeType() {
        return serviceCodeType;
    }

    public String getServiceCodeNormFlg() {
        return serviceCodeNormFlg;
    }

    public void setNormFlg(String normFlg) {
        this.normFlg = normFlg;
    }

    public void setServiceCodeType(String serviceCodeType) {
        this.serviceCodeType = serviceCodeType;
    }

    public void setServiceCodeNormFlg(String serviceCodeNormFlg) {
        this.serviceCodeNormFlg = serviceCodeNormFlg;
    }

    public String getMarketingTitleName() {
        return marketingTitleName;
    }

    public void setMarketingTitleName(String marketingTitleName) {
        this.marketingTitleName = marketingTitleName;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }
}
